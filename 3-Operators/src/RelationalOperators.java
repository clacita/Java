
public class RelationalOperators {

	public static void main(String[] args) {
		
		int i1 = 5;
		int i2 = 10;
		
		System.out.println(i1==i2);
		System.out.println(i1!=i2);
		System.out.println(i1>=i2);
		System.out.println(i1<=i2);
		
		//task
		double d1 = 20;
		double d2 = 80;
		double d3 = (d1+d2)*25;
		double d4 = d3%40;
		System.out.println("Remainder is "+d4+" => "+(d4<=20));
		
		
	}

}
